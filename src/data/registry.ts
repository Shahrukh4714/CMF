function generateGeneratedEntries(): RegistryEntry[] {
  const result: RegistryEntry[] = [];
  // Only ebook and 3D tools are still coming soon — all other categories are active.
  const comingSoonCategories = new Set(["ebook", "3d"]);
  // Map format groups to their actual engine — images use "image", docs use "document", etc.
  const categoryEngineMap: Record<string, Engine> = {
    image: "image", document: "document", video: "video", audio: "audio",
    ebook: "image", font: "image", archive: "archive", "3d": "image",
  };
  for (const [category, exts] of Object.entries(FORMAT_GROUPS)) {
    for (const input of exts) {
      for (const output of exts) {
        if (input === output) continue;
        const fi = FORMATS[input];
        const fo = FORMATS[output];
        const bothPopular = (fi?.popular ?? false) && (fo?.popular ?? false);
        result.push({
          slug: `${input}-to-${output}`,
          name: `${(fi?.name || input.toUpperCase())} to ${(fo?.name || output.toUpperCase())}`,
          category,
          inputFormats: [input],
          outputFormats: [output],
          engine: categoryEngineMap[category] || (category as Engine),
          popular: bothPopular,
          comingSoon: comingSoonCategories.has(category),
          autoGenerated: true,
        });
      }
    }
  }
  return result;
}

