/** Build a ToolDefinition from a RegistryEntry */
function buildEntry(entry: RegistryEntry): ToolDefinition {
  const i = fmt(entry.inputFormats[0]);
  const o = fmt(entry.outputFormats[0]);
  const seo = generateSeo(entry, entry.seo);
  const faq = generateFaq(entry, entry.faq);
  const content = generateToolContent({
    slug: entry.slug,
    name: entry.name,
    inputFormat: i,
    outputFormat: entry.outputFormats.length > 0 ? o : null,
    inputFormats: entry.inputFormats,
    outputFormats: entry.outputFormats,
    categoryId: entry.category,
    engine: entry.engine,
  });
  return {
    id: entry.slug,
    slug: entry.slug,
    name: entry.name,
    shortName: generateShortName(entry),
    title: generateTitle(entry),
    description: seo.description,
    longDescription: content.introduction,
    categoryId: entry.category,
    inputFormat: i,
    outputFormat: entry.outputFormats.length > 0 ? o : null,
    inputFormats: entry.inputFormats,
    outputFormats: entry.outputFormats,
    engine: entry.engine,
    processingType: engineToProcessingType(entry.engine),
    popular: entry.popular ?? false,
    comingSoon: entry.comingSoon ?? false,
    seo,
    faq,
    relatedSlugs: [],
    content,
  };
}

function computeSimilarity(a: ToolDefinition, b: ToolDefinition): number {
  let score = 0;
  if (a.categoryId === b.categoryId) score += 2;
  const sharedInputs = a.inputFormats.filter(f => b.inputFormats.includes(f));
  score += sharedInputs.length * 3;
  const sharedOutputs = a.outputFormats.filter(f => b.outputFormats.includes(f));
  score += sharedOutputs.length * 3;
  const aInputInBOutput = a.inputFormats.filter(f => b.outputFormats.includes(f));
  score += aInputInBOutput.length * 2;
  const aOutputInBInput = a.outputFormats.filter(f => b.inputFormats.includes(f));
  score += aOutputInBInput.length * 2;
  const aFamilies = new Set(a.inputFormats.map(f => (FORMATS[f]?.family || "other")));
  const bFamilies = new Set(b.inputFormats.map(f => (FORMATS[f]?.family || "other")));
  for (const family of aFamilies) {
    if (bFamilies.has(family)) score += 1;
  }
  return score;
}

/** Build all tools and post-process (related links) */
function buildAll(): { tools: ToolDefinition[]; map: Map<string, ToolDefinition> } {
  const tools = allEntries.map(buildEntry);
  const map = new Map<string, ToolDefinition>(tools.map((t) => [t.slug, t]));

  // Post-process: compute related tool slugs using similarity scoring
  for (const tool of tools) {
    const scored = tools
      .filter((t) => t.slug !== tool.slug && !t.comingSoon)
      .map((t) => ({ tool: t, score: computeSimilarity(tool, t) }))
      .sort((a, b) => b.score - a.score)
      .slice(0, 12)
      .map((s) => s.tool.slug);
    tool.relatedSlugs = scored;
  }

  return { tools, map };
}

